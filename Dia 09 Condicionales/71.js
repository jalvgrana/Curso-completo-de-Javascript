// Switch

const condicion = 1;

switch (condicion) {
    case 1:
        console.log("El numero es igual a 1");
        break;
    case 2:
        console.log("El numero es igual a 2");
        break;
    case 3:
        console.log("El numero es igual a 3");
        break;
    default:
        console.log("El numero no esta dentro de los parametros indicados");
} 