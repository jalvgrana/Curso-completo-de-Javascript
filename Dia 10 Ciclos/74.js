// Bucle Do While

// la diferencia con el while, es que el do... while se ejecuta por lo menos una vez:

let i = 0;

do {
    console.log(i);
    i++;

} while (i <= 100);

// se ejecuta una vez:

console.log("Ejecuta una vez el do... while...");

let i2 = 250;

do {
  console.log(i2);
  i++;
} while (i2 <= 100);
